def print_day_info(day, part1, part2):
    """
    Logs day's outputs to terminal in formatted manner
    """
    print(f"Day {day}\n\tPart 1: {part1}\n\tPart 2: {part2}\n")

def get_day_info(day, part1, part2):
    """
    Returns string of 'print_day_info' for testing
    """
    return f'Day {day}\n\tPart 1: {part1}\n\tPart 2: {part2}'
